import gulp from 'gulp';
import scsslint from 'gulp-scss-lint';
import { scsslint as config } from '../config-sp';

/*------------------------------------------------------------------
    task
------------------------------------------------------------------*/
gulp.task('scsslint:sp', function() {

    return gulp
        .src(config.src)
        .pipe(scsslint())
    ;

});
