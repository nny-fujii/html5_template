/*------------------------------------------------------------------------------------------------

    scsslint

------------------------------------------------------------------------------------------------*/
var gulp     = require('gulp');
var scsslint = require('gulp-scss-lint');
var config   = require('../config-sp').scsslint;

/*------------------------------------------------------------------
    task
------------------------------------------------------------------*/
gulp.task('scsslint:sp', function() {

    return gulp
        .src(config.src)
        .pipe(scsslint())
    ;

});
